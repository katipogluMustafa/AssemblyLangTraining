		push ds
		xor ax,ax
		push ax

		mov ax, DATASG
		mov DS, ax

		mov bh, SAYI1
		mov bl, SAYI2
		CALL TOPLAMA
		mov SONUC, AX

	    RETF
MAIN    ENDP
CODESG  ENDS
	    END MAIN